#include <unistd.h>

void	ft_print_comb(void);

void	ft_display(int i, int j, int k)
{
	write(1, &i, 1);
	write(1, &j, 1);
	write(1, &k, 1);
	if (i != 55)
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = 48;
	j = 48;
	k = 48;
	while (i <= 55)
	{
		j = i + 1;
		while (j <= 56)
		{
			k = j + 1;
			while (k <= 57)
			{
				ft_display(i, j, k);
				k++;
			}
			j ++;
		}
		i ++;
	}
}

