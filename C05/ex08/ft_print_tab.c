void	ft_print_tab(int **tab)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (i < 10)
	{
		while (j < 10)
		{
			ft_putchar(tab[i][j]);
			ft_putchar(' ');
			j ++;
		}
		ft_putchar('\n');
		j = 0;
		i ++;
	}
}
