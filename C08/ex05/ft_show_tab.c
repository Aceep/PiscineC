#include "ft_stock_str.h"
#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i ++;
	}
	write(1, "\n", 1);
}

void	ft_putchar(int size)
{
	char	c;

	c = size + '0';
	write(1, &c, 1);
	write(1, "\n", 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		ft_putchar(par[i].size);
		ft_putstr(par[i].copy);
		i ++;
	}
}
