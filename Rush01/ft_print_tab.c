#include "Fonctions.h"

void	ft_print_tab(int **tab)
{
	int	i;
	int	j;

	j = 1;
	i = 1;
	while (i < 5)
	{
		while (j < 5)
		{
			ft_putchar(tab[i][j] + 48);
			ft_putchar(' ');
			j ++;
		}
		ft_putchar('\n');
		j = 1;
		i ++;
	}
}
