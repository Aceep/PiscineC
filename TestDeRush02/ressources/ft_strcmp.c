#include "rush02.h"


int	strcmp_nbr(char *nbr1, char *nbr2)
{
	int	i;

	i = 0;
	if (*nbr2 == '0' && *(nbr2 + 1) != ':' && *(nbr2 + 1) != ' ')
	{
		while (*nbr2 == '0')
			nbr2++;
	}
	while (nbr1[i] && nbr2[i] != ' ' && nbr2[i] != ':' && nbr1[i] == nbr2[i])
		i++;
	if (!nbr1[i] && (nbr2[i] == ' ' || nbr2[i] == ':'))
		return (1);
	return (0);
}
